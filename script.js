const printVideos = (listVideos, contenedor) => {
  //1. Vaciemos el contenido del contenedor
  contenedor.innerHTML = "";

  listVideos.forEach(videos => {
    const article = document.createElement('article');
    article.classList.add("main__videos");
    article.innerHTML =
      `<article class="main__videos">
          <figure class="videos__image">
              <img src=${videos.Video} alt="name">
          </figure>
          <div class="info__video">
          <a>${videos.nombreVideo}</a>
          <p>${videos.nombreAutor}</p>
          <p>${videos.visitasVideo}</p>
      </div>
      </article >
      `;
    contenedor.appendChild(article);
  });
}


